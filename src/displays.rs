pub fn make_map(room: RoomID) {
    println!("                        ###### ______");
    if room == RoomID(1) {
        println!("                        # *W #       ||");
    } else {
        println!("                        # W  #       ||");
    }
    println!("                        ######      ###### ");

    if room == RoomID(2) {
        println!("                          ||        # *VR# ");
    } else {
        println!("                          ||        # VR # ");
    }
    println!("                        ######      ###### ");
    if room == RoomID(0) {
        println!("                        # *S # ______||");
    } else {
        println!("                        # S  # ______||");
    }
    println!("                        ######");
    println!("                          ||  ");
    println!("######______######______######");

    if room == RoomID(3) {
        println!("# E  #______# SS #______# *L #");
    } else if room == RoomID(4) {
        println!("# E  #______# *SS#______# L  #");
    } else if room == RoomID(5) {
        println!("# *E #______# SS #______# L  #");
    } else {
        println!("# E  #______# SS #______# L  #");
    }
    println!("######      ######      ######");
    if room == RoomID(6) {
        println!(" | |         |*|");
    } else {
        println!(" | |         | |");
    }

    println!("######      ######");
    if room == RoomID(7) {
        println!("# *B #");
    } else {
        println!("# B  #");
    }
    println!("######");
}



